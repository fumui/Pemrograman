package uts;

public class Loop {
    public static void main(String[] args) {
        int n = 10;
        while (n > 0){
            System.out.println(n);
            n--;
        }
        n = 10;
        while (n > 0){
            System.out.println("KAMPUS UNPAM");
            n--;
        }
    }
}
